
export  function darkTheme() {
  document.documentElement.style.setProperty("--primary-color", "#1E2124");
  document.documentElement.style.setProperty("--third-color", "#2E3136");
  document.documentElement.style.setProperty("--fourth-color", "#ededed");
  document.documentElement.style.setProperty("--fifth-color", "#c5bfbf");
  document.documentElement.style.setProperty("--sixth-color", "#36393E");
  document.documentElement.style.setProperty("--seventh-color", "#232428");
  document.documentElement.style.setProperty("--eighth-color", "#787878");
  localStorage.setItem("--primary-color", "#1E2124");
  localStorage.setItem("--third-color", "#2E3136");
  localStorage.setItem("--fourth-color", "#ededed");
  localStorage.setItem("--fifth-color", "#c5bfbf");
  localStorage.setItem("--sixth-color", "#36393E");
  localStorage.setItem("--seventh-color", "#232428");
  localStorage.setItem("--eighth-color", "#787878");
}
